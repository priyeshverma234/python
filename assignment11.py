#Q.1- Write python code to find a valid email address from a text?
'''
Answer :-
[a-zA-Z0-9_.]*[@](gmail.com|yahoo.com)
'''

#Q.2- Write python program to find a valid Indian phone number from a text
# (Valid Indian number will start with "+91-" and after that [6-9] followed by 9 digits)
'''
Answer:-
^(+91-)[6-9]\d{9}
'''
